# 列表和字符串是有序结构都能切片，而集和字典无序结构，没有切片
a = [i for i in range(10)]
print(a)
# a[x:y：z],x,y是下标序号，从x开始到y结束，不包括y，z是切片间隔。如果x,y是负数，则为负数对应的下标，但负数下标从-1开始，当不存于这个范围，返回空列表
print(a[:5], a[5:-100], a[2:8])
# a[::x]是间隔切片，省略y，默认取最大区间，负号代表从末尾开始
print(a[::-1], a[::1], a[::2], a[::-2], a[0:10:-2], a[0::-2])
# 切片操作不能出现自相矛盾，否则返回空
print(a[0:100:-1], a[6:2])
